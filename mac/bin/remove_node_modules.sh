#!/bin/bash

# Recursively remove all node_modules directories from a given path (default: current directory)

TARGET="${1:-.}"

if [ ! -d "$TARGET" ]; then
    echo "Error: '$TARGET' is not a directory"
    exit 1
fi

echo "Searching for node_modules in: $(realpath "$TARGET")"

find "$TARGET" -type d -name "node_modules" -prune | while IFS= read -r dir; do
    echo "Removing: $dir"
    rm -rf "$dir"
done

echo "Done."
